function setCaretState(caretEl, { visible, blinking }) {
  if (!caretEl) return;
  caretEl.style.visibility = visible ? "visible" : "hidden";
  caretEl.style.opacity = visible ? "1" : "0";
  caretEl.style.animationPlayState = blinking ? "running" : "paused";
}

function typeText(element, newText, caretEl) {
  return new Promise((resolve) => {
    const oldText = element.textContent || "";

    // reset/hide caret before animating
    if (caretHideTimeout) clearTimeout(caretHideTimeout);
    setCaretState(caretEl, { visible: true, blinking: false });

    // clear instantly, then type in (prevents lone punctuation floating)
    element.textContent = "";
    let j = 0;
    const type = () => {
      if (j <= newText.length) {
        element.textContent = newText.slice(0, j);
        j += 1;
        requestAnimationFrame(() => setTimeout(type, TYPE_SPEED));
      } else {
        // blink briefly, then hide after ~1.5s
        setCaretState(caretEl, { visible: true, blinking: true });
        caretHideTimeout = setTimeout(() => {
          setCaretState(caretEl, { visible: false, blinking: false });
        }, 1500);
        resolve();
      }
    };
    type();
  });
}

function normalizeLanguage(rawLang) {
  if (!rawLang) return "en";
  const lower = rawLang.toLowerCase();
  if (lower.startsWith("zh")) return "zh";
  if (lower.startsWith("ja")) return "ja";
  if (lower.startsWith("ko")) return "ko";
  return "en";
}

async function setLanguage(lang, { persist = true } = {}) {
  const normalized = normalizeLanguage(lang);
  const strings = translations[normalized] ?? translations.en;
  activeLanguage = normalized;

  const titleTextEl =
    document.querySelector("#hero-title .typewriter-text") ||
    document.getElementById("hero-title");
  const caretEl = document.querySelector("#hero-title .caret");
  const subtitleEl = document.getElementById("hero-subtitle");

  document.documentElement.lang = normalized;

  const buttons = document.querySelectorAll("[data-lang]");
  for (const button of buttons) {
    const isActive = button.getAttribute("data-lang") === normalized;
    button.setAttribute("aria-pressed", String(isActive));
  }

  if (titleTextEl) {
    await typeText(titleTextEl, strings.title, caretEl);
  }

  if (subtitleEl) {
    subtitleEl.classList.remove("fade-in", "fade-out");
    // force reflow so transitions retrigger
    void subtitleEl.offsetWidth;
    subtitleEl.classList.add("fade-out");
    setTimeout(() => {
      subtitleEl.textContent = strings.subtitle;
      subtitleEl.classList.remove("fade-out");
      subtitleEl.classList.add("fade-in");
      setTimeout(() => subtitleEl.classList.remove("fade-in"), 300);
    }, 140);
  }

  // update all other translatable text nodes
  applyStaticTranslations(strings);
  rerenderLanguagePicker();
  rerenderSiteMenu();
  rerenderAboutMe();
  rerenderCourseworkPage();
  rerenderProgrammingProjects();
  rerenderCreativeProjects();

  if (persist) localStorage.setItem(STORAGE_KEY, normalized);
}

